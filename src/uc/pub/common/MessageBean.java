package uc.pub.common;

import java.io.Serializable;
import java.util.HashMap;
import java.util.HashSet;

public class MessageBean implements Serializable {

	private String name;//登录名
	private String pwd;//密码
	private int type; // 1私聊 0上下线更新 -1下线请求 2请求发送文件 3.确定接收文件
	
	private HashSet<String> clients; // 存放选中的客户
	private HashSet<String> to;	
	
	private String info;//内容
	private String timer;//时间	
	private String fileName;//文件名称
	private String ip;//ip地址
	private int size;//大小
	private int port;//端口号	
		

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public HashSet<String> getTo() {
		return to;
	}

	public void setTo(HashSet<String> to) {
		this.to = to;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public HashSet<String> getClients() {
		return clients;
	}

	public void setClients(HashSet<String> clients) {
		this.clients = clients;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getTimer() {
		return timer;
	}

	public void setTimer(String timer) {
		this.timer = timer;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
	
}
